#include "StopState.h"
#include "RunningState.h"

//*****************************************************************************
// クラス定義
//*****************************************************************************
#define ACTIVE_TIME	(180)

//=============================================================================
// コンストラクタ
//=============================================================================
StopState::StopState(Player* owner) : owner_(owner)
{
	cnt = 0;
}

//=============================================================================
// デストラクタ
//=============================================================================
StopState::~StopState()
{
}

//=============================================================================
// 更新
//=============================================================================
void StopState::Update(int AnimCurtID)
{
	cnt++;

	if (cnt == ACTIVE_TIME)
	{
		cnt = 0;
		owner_->ChangeAnim(Running);
		owner_->ChangeState(new RunningState(owner_));
		return;
	}
}