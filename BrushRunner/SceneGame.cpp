#include "Main.h"
#include "SceneGame.h"
#include "Map.h"
#include "Camera.h"

// オブジェクトのポインタ
MAP *pMap;

//=============================================================================
// 初期化
//=============================================================================
HRESULT InitSceneGame()
{
	pMap = new MAP();

	return S_OK;
}

//=============================================================================
// 終了
//=============================================================================
void UninitSceneGame()
{
	delete pMap;
}

//=============================================================================
// 更新
//=============================================================================
void UpdateSceneGame()
{
	UpdateCamera();

	pMap->Update();
}

//=============================================================================
// 描画
//=============================================================================
void DrawSceneGame()
{
	pMap->Draw();
}